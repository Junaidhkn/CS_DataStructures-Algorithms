// A linked list is the most sought-after data structure when it comes to handling dynamic data elements. A linked list consists of a data element known as a node. And each node consists of two fields: one field has data, and in the second field, the node has an address that keeps a reference to the next node.

// Linked list does not have indexes like arrays. So, if you want to access a particular node, you need to start from the head and follow the next references until you get that node.

// Linked list is a linear data structure, which means that its elements are stored in a non-contiguous way. Each element of the list is stored in a separate memory location. The elements are linked using pointers.
// It has head and tail pointers, and each node has a next pointer that points to the next node in the list. null is the value of the next pointer of the last node of the list.

